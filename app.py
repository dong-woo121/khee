# 행정구역 자동 매핑
REGION_PARENT = {
    region: group
    for group, regions in REGION_GROUPS.items()
    for region in regions
}

TRUCK_SIZES   = [0.5,   1.0,   2.5,    5.0]
MAX_BOXES_375 = [100,   200,   500,   1000]
MAX_BOXES_750 = [52.5,  105,   262.5,  525]
MANUAL_FEE    = [20000, 20000, 30000, 50000]   # 0.5t는 1t 준용

VIA_FEE_SAME  = [20000, 20000, 20000, 30000]   # 동일 행정구역 (0.5t는 1t 준용)
VIA_FEE_DIFF  = [20000, 20000, 30000, 50000]   # 타 행정구역   (0.5t는 1t 준용)

PRODUCTS_375 = ["22 375", "22 375K", "38 375", "38 375K"]
PRODUCTS_750 = ["22 750", "22 750K", "38 750", "38 750K"]


def box_inputs(products: list[str], key_prefix: str) -> int:
    cols = st.columns(len(products))
    total = 0
    for col, product in zip(cols, products):
        with col:
            total += st.number_input(
                product, min_value=0, step=1, value=0, key=f"{key_prefix}_{product}"
            )
    return total


def find_truck(b375: int, b750: int, region: str):
    """최소 적합 차량 인덱스와 적재율 반환. 최대 차량도 초과 시 (None, 초과율) 반환."""
    rates = RATES[region]
    last_valid = max(i for i, r in enumerate(rates) if r is not None)
    for i in range(len(TRUCK_SIZES)):
        if rates[i] is None:
            continue
        ratio = (b375 / MAX_BOXES_375[i]) + (b750 / MAX_BOXES_750[i])
        if ratio <= 1.0:
            return i, ratio
    ratio = (b375 / MAX_BOXES_375[last_valid]) + (b750 / MAX_BOXES_750[last_valid])
    return None, ratio


